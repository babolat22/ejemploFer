    private void BtGuardaEmpresaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtGuardaEmpresaActionPerformed
        if (validarCamposVacios(pnlEmpresa)) {
            emp = new Empresa();
            emp.setRazonSocial(jTFRazon.getText()); 

            emp.setCUIT(Integer.parseInt(jTFCUIT.getText()));
            //emp.setListaEmpleado(null);
             if(!empresas.contains(emp)){
                 empresas.add(emp);
                 System.out.println("agregado!");
                 jComboBoxEmpresa.addItem(emp.getRazonSocial());
                 btnMostrarEmp.setEnabled(true);
             }else
                 JOptionPane.showMessageDialog(pnlEmpresa,"La empresa ya existe!!");
            BtGuardaEmpleado.setEnabled(true);
            vaciarCampos(pnlEmpresa); //opcion2 THE BEST!
        }else
            JOptionPane.showMessageDialog(pnlEmpresa, "Los campos no deben estar vacios!...");
    }//GEN-LAST:event_BtGuardaEmpresaActionPerformed

