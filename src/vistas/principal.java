    private void BtGuardaEmpleadoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtGuardaEmpleadoActionPerformed
        if (validarCamposVacios(pnlEmpleado)) {
          e1 = new Empleado(Integer.parseInt(jTFDni.getText()), jTFnombre.getText(), jTFApe.getText(), jComboBoxCat.getSelectedItem().toString(), Double.parseDouble(jTFSueldo.getText()));
          for(int i=0; i<empresas.size(); i++){
            if(empresas.get(i).getRazonSocial() == jComboBoxEmpresa.getSelectedItem().toString()){
                e1.setTrabaja(empresas.get(i));  // toda la empresa
                JOptionPane.showMessageDialog(rootPane, "trabaja en "+e1.getTrabaja().getRazonSocial());
                
                empresas.get(i).agregaEmpleado(e1);
                JOptionPane.showMessageDialog(rootPane, "añadido a la empresas "+empresas.get(i).getRazonSocial());
                
            }
          }
          if(!listaEmp.contains(e1)) listaEmp.add(e1); 
          //limpiarEmpleado();       //opcion1
          vaciarCampos(pnlEmpleado); //opcion2 THE BEST!
        } else
            JOptionPane.showMessageDialog(pnlEmpleado, "Los campos no deben estar vacios!...");
    }//GEN-LAST:event_BtGuardaEmpleadoActionPerformed

